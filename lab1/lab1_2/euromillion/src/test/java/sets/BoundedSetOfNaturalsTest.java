package sets;

import static org.junit.jupiter.api.Assertions.*;

import org.junit.jupiter.api.*;
import pt.deti.ua.sets.BoundedSetOfNaturals;

/**
 * @author ico0
 */
class BoundedSetOfNaturalsTest {
    private BoundedSetOfNaturals setA;
    private BoundedSetOfNaturals setB;
    private BoundedSetOfNaturals setC;


    @BeforeEach
    public void setUp() {
        setA = new BoundedSetOfNaturals(1);
        setB = BoundedSetOfNaturals.fromArray(new int[]{10, 20, 30, 40, 50, 60});
        setC = BoundedSetOfNaturals.fromArray(new int[]{50, 60});
    }

    @AfterEach
    public void tearDown() {
        setA = setB = setC = null;
    }

    @DisplayName("Check if a element is inside of a set")
    @Test
    public void testAddElement() {

        setA.add(99);
        assertTrue(setA.contains(99), "add: added element not found in set.");
        assertEquals(1, setA.size());

        assertThrows(IllegalArgumentException.class, () -> setB.add(70));
        assertFalse(setB.contains(70), "add: added element not found in set.");
        assertEquals(setB.size(), 6);
    }

    @DisplayName("The elements inside the set should be only positive")
    @Test
    public void testAddFromBadArray() {
        int[] elems = new int[]{10, -20, -30};

        // must fail with exception
        assertThrows(IllegalArgumentException.class, () -> setA.add(elems));
    }

    @DisplayName("Check if the intersects method functions properly")
    @Test
    public void testIntersects() {
        BoundedSetOfNaturals setD = new BoundedSetOfNaturals(1);
        assertTrue(setB.intersects(setC));
        assertFalse(setB.intersects(setD));
    }


}
